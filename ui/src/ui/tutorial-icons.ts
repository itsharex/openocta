/** 分类 icon 默认背景色（按索引循环）：橙、蓝、绿、紫、红 */
const DEFAULT_ACCENT_COLORS: Array<{ color: string; subtle: string }> = [
  { color: "#f97316", subtle: "rgba(249,115,22,0.2)" },
  { color: "#3b82f6", subtle: "rgba(59,130,246,0.2)" },
  { color: "#22c55e", subtle: "rgba(34,197,94,0.2)" },
  { color: "#8b5cf6", subtle: "rgba(139,92,246,0.2)" },
  { color: "#ef4444", subtle: "rgba(239,68,68,0.2)" },
];

export function accentVars(accent?: string, index?: number): string {
  const key = (accent ?? "").trim().toLowerCase();
  if (key === "green" || key === "violet" || key === "orange") {
    const color =
      key === "green"
        ? "#22c55e"
        : key === "violet"
          ? "#8b5cf6"
          : "#f97316";
    const subtle =
      key === "green"
        ? "rgba(34,197,94,0.14)"
        : key === "violet"
          ? "rgba(139,92,246,0.14)"
          : "rgba(249,115,22,0.14)";
    return `--accent:${color};--accent-subtle:${subtle};`;
  }
  const def = DEFAULT_ACCENT_COLORS[(index ?? 0) % DEFAULT_ACCENT_COLORS.length];
  return `--accent:${def.color};--accent-subtle:${def.subtle};`;
}
